x = "Hello, World"
print(x)

x = "\tThis string starts with a \"tab\"."
print(x)

x = "This string contains a single backslash(\\)."
print(x)

# Use single quotes or double quotes
x = "Hello, World"
x = 'Hello, World'

# triple-quoted string
x = """Starting and ending a string with triple " characters
permits embedded newlines, and the use of " and ' without
backslashes"""

# triple-single-quoted string
y = '''Starting and ending a string with triple " characters
permits embedded newlines, and the use of " and ' without
backslashes'''

print(x)
print(y)